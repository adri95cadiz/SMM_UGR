    @Override
    public void paint (Graphics g){
        super.paint(g);
        g.setColor(this.color);
        
        switch (forma){
            case PUNTO: {
                if( this.pClick == null) { break; }
                g.fillOval(this.pClick.x - 2, this.pClick.y - 2, 4, 4);
                break;
            }
            case LINEA: {
                if( this.pIni == null || this.pFin == null) { break; }
                g.drawLine(this.pIni.x, this.pIni.y, this.pFin.x, this.pFin.y);
                break;
            }
            case RECTANGULO: {
                if( this.pIni == null || this.pFin == null) { break; }
                int x = Math.min(this.pIni.x, this.pFin.x);
                int y = Math.min(this.pIni.y, this.pFin.y);
                int ancho = Math.abs(this.pIni.y - this.pFin.y);
                int alto = Math.abs(this.pIni.x - this.pFin.x);
                
                if (this.relleno) {
                    g.fillRect(x, y, alto, ancho);
                    break;
                }
                g.drawRect(x, y, alto, ancho);
                break;
            }
            case ELIPSE: {
                if( this.pIni == null || this.pFin == null) { break; }
                int x = Math.min(this.pIni.x, this.pFin.x);
                int y = Math.min(this.pIni.y, this.pFin.y);
                int ancho = Math.abs(this.pIni.y - this.pFin.y);
                int alto = Math.abs(this.pIni.x - this.pFin.x);
                
                if (this.relleno) {
                    g.fillOval(x, y, alto, ancho);
                    break;
                }
                g.drawOval(x, y, alto, ancho);
                break;
            }
        }
    }
    
    public void clearPoints(){
        this.pFin = null;
        this.pIni = null;
        this.pClick = null;
    }
    
