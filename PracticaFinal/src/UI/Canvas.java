    @Override
    public void paint(Graphics g)
    {
        super.paint(g);
        Graphics2D g2d = (Graphics2D)g;
        if(image != null) g.drawImage(image,0,0,this);
        this.draw(g2d);
    }
    
    private void draw(Graphics2D g2d)
    {
        this.paintShapeVector(g2d); 
        this.paintTextVector(g2d);
    }   

    private void paintShapeVector(Graphics2D g2d)
    {
        // Activamos el antialiasing GLOBAL
        if (Config.GENERALCONFIG.getAntialiasing())
            g2d.setRenderingHints(new RenderingHints(
                    RenderingHints.KEY_ANTIALIASING,
                    RenderingHints.VALUE_ANTIALIAS_ON
            ));

        // Activamos el canal alfa GLOBAL
        g2d.setComposite(AlphaComposite.getInstance(AlphaComposite.SRC_OVER, (float) Config.GENERALCONFIG.getAlpha() / 100));

        if (this.clip != null)
            g2d.clip(this.clip);

        // Pintamos las figuras del vector
        for (graphics.Shape s : this.shapes)
        {
            s.draw(g2d);
            if (s.equals(this.selectedShape))
            {
                g2d.setColor(Color.BLUE);
                float dash1[] =
                {
                    3.0f
                };
                g2d.setStroke(new BasicStroke(1.0f,
                        BasicStroke.CAP_BUTT,
                        BasicStroke.JOIN_MITER,
                        3.0f, dash1, 0.0f));
                g2d.drawRect(s.getBounds().x - 1, s.getBounds().y - 1, s.getBounds().width + 2, s.getBounds().height + 2);
            }

        }
    }
    
    private void paintTextVector(Graphics2D g2d)
    { 
        // Activamos el antialiasing GLOBAL
        if (Config.GENERALCONFIG.getAntialiasing())
            g2d.setRenderingHints(new RenderingHints(
                    RenderingHints.KEY_ANTIALIASING,
                    RenderingHints.VALUE_ANTIALIAS_ON
            ));
        // Activamos el canal alfa GLOBAL
        g2d.setComposite(AlphaComposite.getInstance(AlphaComposite.SRC_OVER, (float) Config.GENERALCONFIG.getAlpha() / 100));
        
        if (this.clip != null)
            g2d.clip(this.clip);
        
        for (int i = 0; i<textArray.size(); i++)
        {
            g2d.setFont(textArray.get(i).getFont());
            g2d.setColor(textArray.get(i).getColor());
            g2d.drawString(textArray.get(i).getString(), textArray.get(i).getStartPoint().x, textArray.get(i).getStartPoint().y);
        }
    }
    
    public Image getImage() {
        return image;
    }

    public void setImage(Image image) {
        this.image = image;
    }
    
    public Shape getSelectedShape()
    {
        return this.selectedShape;
    }

    private graphics.Shape getSelectedShape(Point2D p)
    {
        for (graphics.Shape s : this.shapes)
            if (s.contains(p))
                return s;

        return null;

    }

    protected void setClip(java.awt.Shape clip)
    {
        this.clip = clip;
    }
    
    public void setSelectedShape(int index)
    {
      
        if ( this.shapes.size() > 0 && this.shapes.size() > index)

        try
        {
            this.selectedShape = this.shapes.get(index);

           
        } catch (Exception ex)
        {
        }
            
              
        this.repaint();
    }

